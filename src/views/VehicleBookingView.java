    private void jButton14ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton14ActionPerformed
        // TODO add your handling code here:
         VehicleBooking booking = new VehicleBooking();
        if (Validacao.notNull(this.jTXTClienteEditar.getText())){
            booking.setClient_idclient(Integer.parseInt(this.jTXTClienteEditar.getText()));
            if (Validacao.notNull(this.jTXTContratoEditar.getText())){
                booking.setContract_idcontract(Integer.parseInt(this.jTXTContratoEditar.getText()));
                if (Validacao.notNull(this.jTXTVeiculoEditar.getText())){
                    booking.setVehicle_idvehicle(Integer.parseInt(this.jTXTVeiculoEditar.getText()));
                    VehicleBookingController controller = new VehicleBookingController();
                    controller.update(booking, this.bookingID);
                }
                else{
                    JOptionPane.showMessageDialog(null, "Entre com o ID do veiculo!");
                this.jTXTVeiculoEditar.requestFocus();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Entre com o ID do contrato!");
                this.jTXTContratoEditar.requestFocus();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Entre com o ID do cliente!");
            this.jTXTClienteEditar.requestFocus();
        }
        
    }//GEN-LAST:event_jButton14ActionPerformed

